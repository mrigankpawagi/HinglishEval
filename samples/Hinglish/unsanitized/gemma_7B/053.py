def add(x: int, y: int):
    """
    Do numbers x aur y ko jodo
    >>> add(2, 3)
    5
    >>> add(5, 7)
    12
    """
    return x + y


def sub(x: int, y: int):
    """
    Do numbers x aur y ko jodo
    >>> sub(2, 3)
    -1
    >>> sub(5, 7)
    -2
    """
    return x - y


def mul(x: int, y: int):
    """
    Do numbers x aur y ko jodo
    >>> mul(2, 3)
    6
    >>> mul(5, 7)
    35
    """
    return x * y


def div(x: int, y: int):
    """
    Do numbers x aur y ko jodo
    >>> div(2, 3)
    0.6666666666666666
    >>> div(5, 7)
    0.7142857142857143
    """
    return x / y
